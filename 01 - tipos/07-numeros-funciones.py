import math

print(round(1.3))
print(round(1.5))  # Redondea al valor par más cercano
print(abs(-1.3))  # Valor absoluto
print(abs(1.3))  # Valor absoluto

print(math.ceil(1.3))  # Redondea hacia arriba
print(math.floor(1.3))  # Redondea hacia abajo
print(math.isnan(23))  # Comprueba si es un número
# print(math.isnan("23"))  # Comprueba si es un número
print(math.pow(10, 3))  # Potencia
print(math.sqrt(9))  # Raíz cuadrada
